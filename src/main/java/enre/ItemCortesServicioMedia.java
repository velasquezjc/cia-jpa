package enre;

import java.util.Date;

public class ItemCortesServicioMedia {
	private String partido;
	private String localidad;
	private String subestacion_alimentador;
	private String usuarios;
	private String normalizacion;
	
	public String getPartido() {
		return partido;
	}
	public void setPartido(String partido) {
		this.partido = partido;
	}
	public String getLocalidad() {
		return localidad;
	}
	public void setLocalidad(String localidad) {
		this.localidad = localidad;
	}
	public String getSubestacion_alimentador() {
		return subestacion_alimentador;
	}
	public void setSubestacion_alimentador(String subestacion_alimentador) {
		this.subestacion_alimentador = subestacion_alimentador;
	}
	public String getUsuarios() {
		return usuarios;
	}
	public void setUsuarios(String usuarios) {
		this.usuarios = usuarios;
	}
	public String getNormalizacion() {
		return normalizacion;
	}
	public void setNormalizacion(String normalizacion) {
		this.normalizacion = normalizacion;
	}
	

}
